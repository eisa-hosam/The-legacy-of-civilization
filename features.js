  function initSuggestionBox() {
    const form = document.getElementById('suggestion-form');
    if (!form) return;
    const openBtn = document.getElementById('suggestion-box-btn');
    const loginRequiredEl = document.getElementById('suggestion-login-required');
    const loginRequiredBtn = document.getElementById('suggestion-login-btn');
    const asNameEl = document.getElementById('suggestion-as-name');
    const textInput = document.getElementById('suggestion-text');
    const errorEl = document.getElementById('suggestion-error');
    const successEl = document.getElementById('suggestion-success');
    const submitBtn = document.getElementById('suggestion-submit-btn');
    const suggestionModal = document.getElementById('suggestion-modal');
    const loginModal = document.getElementById('login-modal');

    function isLoggedIn() {
      return typeof currentUser !== 'undefined' && !!currentUser;
    }

    function refreshGate() {
      if (isLoggedIn()) {
        loginRequiredEl.style.display = 'none';
        form.style.display = 'block';
        if (asNameEl) asNameEl.textContent = currentUser.username || currentUser.email || 'زائر مسجّل';
      } else {
        loginRequiredEl.style.display = 'block';
        form.style.display = 'none';
      }
    }

    openBtn?.addEventListener('click', refreshGate);

    loginRequiredBtn?.addEventListener('click', () => {
      if (suggestionModal) {
        suggestionModal.classList.remove('active');
      }
      if (loginModal) {
        loginModal.classList.add('active');
        document.body.style.overflow = 'hidden';
      }
    });

    form.addEventListener('submit', async (e) => {
      e.preventDefault();
      errorEl.classList.remove('show');
      successEl.classList.remove('show');

      if (!isLoggedIn()) {
        refreshGate();
        return;
      }

      const text = (textInput.value || '').trim();
      if (text.length < 5) {
        errorEl.textContent = 'اكتب اقتراح لا يقل عن 5 أحرف من فضلك.';
        errorEl.classList.add('show');
        return;
      }
      submitBtn.disabled = true;
      submitBtn.textContent = 'جارٍ الإرسال...';
      const payload = {
        text: text,
        name: currentUser.username || currentUser.email || 'زائر مسجّل',
        uid: currentUser.uid,
        page: location.href,
        createdAt: (typeof firebase !== 'undefined' && firebase.firestore && firebase.firestore.FieldValue)
          ? firebase.firestore.FieldValue.serverTimestamp() : new Date().toISOString()
      };
      try {
        if (typeof db !== 'undefined' && db) {
          await db.collection('suggestions').add(payload);
        } else {
          throw new Error('no-db');
        }
      } catch (err) {
        /* لو فشل الاتصال بـ Firebase، نخزن الاقتراح محليًا مؤقتًا عشان ميضاعش */
        try {
          const key = 'museum_pending_suggestions';
          const pending = JSON.parse(localStorage.getItem(key) || '[]');
          pending.push(payload);
          localStorage.setItem(key, JSON.stringify(pending));
        } catch (e2) {}
      }
      submitBtn.disabled = false;
      submitBtn.textContent = 'إرسال الاقتراح';
      successEl.classList.add('show');
      form.reset();
      setTimeout(() => successEl.classList.remove('show'), 4000);
    });
  }

