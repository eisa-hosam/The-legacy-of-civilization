  function getWeekKey(date) {
    /* الأسبوع يبدأ يوم السبت (أول أيام الأسبوع في مصر والعالم العربي) */
    const d = new Date(date);
    const day = d.getDay(); /* الأحد=0 ... السبت=6 */
    const diffToSaturday = (day + 1) % 7;
    const weekStart = new Date(d);
    weekStart.setDate(d.getDate() - diffToSaturday);
    weekStart.setHours(0, 0, 0, 0);
    return weekStart.toISOString().slice(0, 10);
  }

  function weekRangeLabel(weekKeyStr) {
    const start = new Date(weekKeyStr);
    const end = new Date(start);
    end.setDate(start.getDate() + 6);
    const fmt = (d) => d.toLocaleDateString('ar-EG', { day: 'numeric', month: 'long' });
    return `${fmt(start)} – ${fmt(end)}`;
  }

  function renderWeeklyChampion() {
    const mount = document.getElementById('weekly-champion-content');
    if (!mount || typeof db === 'undefined') return;
    const weekKey = getWeekKey(new Date());
    db.collection('daily_leaderboard')
      .where('weekKey', '==', weekKey)
      .get()
      .then(snap => {
        const tally = {};
        snap.forEach(doc => {
          const d = doc.data();
          if (!d.correct) return;
          if (!tally[d.uid]) tally[d.uid] = { username: d.username || 'زائر', count: 0, firstTs: d.ts || Date.now() };
          tally[d.uid].count++;
          if (d.ts && d.ts < tally[d.uid].firstTs) tally[d.uid].firstTs = d.ts;
        });
        const ranked = Object.entries(tally)
          .map(([uid, v]) => ({ uid, ...v }))
          .sort((a, b) => b.count - a.count || a.firstTs - b.firstTs);

        if (!ranked.length) {
          mount.innerHTML = `<p class="weekly-empty">لسّه محدش سجّل إجابة صح الأسبوع ده — يلا كن أول بطل الأسبوع! 🏆</p>`;
          return;
        }
        const champion = ranked[0];
        const isMe = typeof currentUser !== 'undefined' && currentUser && currentUser.uid === champion.uid;
        mount.innerHTML = `
          <div class="weekly-champion-card">
            <div class="weekly-champion-crown">🏆</div>
            <p class="weekly-champion-name">${champion.username}</p>
            <p class="weekly-champion-meta">${champion.count} إجابة صحيحة هذا الأسبوع (${weekRangeLabel(weekKey)})</p>
            ${isMe
              ? `<button type="button" class="btn btn-gold btn-sm" id="download-cert-btn">🎖️ نزّل شهادتك</button>`
              : `<p class="weekly-champion-hint">جاوب صح أكتر عشان توصل لقمة الأسبوع!</p>`}
          </div>
        `;
        if (isMe) {
          document.getElementById('download-cert-btn')?.addEventListener('click', () => {
            downloadWeeklyCertificate(champion.username, weekRangeLabel(weekKey), champion.count);
          });
        }
      })
      .catch(() => { mount.innerHTML = '<p class="weekly-empty">تعذّر تحميل بطل الأسبوع حاليًا.</p>'; });
  }

  function wrapCertText(ctx2d, text, x, y, maxWidth, lineHeight) {
    const words = String(text).split(' ');
    let line = '', lines = [];
    words.forEach(word => {
      const test = line ? line + ' ' + word : word;
      if (ctx2d.measureText(test).width > maxWidth && line) { lines.push(line); line = word; }
      else line = test;
    });
    if (line) lines.push(line);
    lines.forEach((l, i) => ctx2d.fillText(l, x, y + i * lineHeight));
  }

